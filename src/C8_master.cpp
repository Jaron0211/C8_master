C8_master::~C8_master(){
    if(C8_Serial != NULL) free(C8_master::C8_Serial);
}

void C8_master::init(int baudrate)
{
  C8_master::C8_Serial = new SoftwareSerial(C8_master::com_port_tx,C8_master::com_port_rx);
  C8_master::C8_Serial -> begin(baudrate);
}

float C8_master::ActiveReadCO2() {

    float return_int[1];
    int raw_data[16];
    int CO2_index = -1;

    for ( int i = 0; i < sizeof(C8_master::send_command); i++){
        C8_master::C8_Serial -> write(C8_master::send_command[i]);
    }

    delay(10);

    String _buffer = "";
    while(C8_master::C8_Serial -> available()){
        char _buf = C8_master::C8_Serial -> read();
        _buffer += _buf;
    }

    if(sizeof(raw_data) > 0){
        CO2_index = raw_data[4] << 8 | raw_data[5];
    }

    C8_master::C8_Serial -> flush();

    return CO2_index;

}

float C8_master::ReadCO2() {

    String _buffer;
    int raw_data[16];
    int CO2_index = -1;

    if (C8_master::C8_Serial -> available() >= 16 && (millis() - C8_master::timer > 600)) {
        C8_master::timer = millis();
        while(C8_master::C8_Serial -> available()){
            char _buf = C8_master::C8_Serial -> read();
            _buffer += _buf;
        }

        if(sizeof(_buffer) > 0 ){
            
            //Issue #1 https://github.com/Jaron0211/C8_master/issues/1#issue-1931603183
            String _head_str = "";
            _head_str += char(0x42);
            _head_str += char(0x4D);
            _head_str += char(0x0C);
            _head_str += char(0x51);
            int header_index = _buffer.indexOf(_head_str);

            if (sizeof(_buffer) - header_index >= 16 ){
                for (int index = header_index; index < (16+header_index); index++){
                    raw_data[index-header_index] = _buffer[index];
                }
            }

            if(sizeof(raw_data) > 0){
                CO2_index = raw_data[6] << 8 | raw_data[7];
            }
            C8_master::C8_Serial -> flush();
        }
    }
    return CO2_index;

}

